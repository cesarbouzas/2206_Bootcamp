ID INT4 generated by DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
DESCRIPTION VARCHAR(255) NOT NULL
);
CREATE TABLE STATUS(
ID INT4 generated by DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
DESCRIPTION VARCHAR(255) NOT NULL
);
CREATE TABLE ORIGIN (
ID INT4 generated by DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
DESCRIPTION VARCHAR(255) NOT NULL
);
CREATE TABLE EXPERIENCE_LEVEL (
ID INT4 generated BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
DESCRIPTION VARCHAR(255) NOT NULL
);
CREATE TABLE PROFILE (
ID INT4 generated by default as IDENTITY PRIMARY KEY NOT NULL,
DESCRIPTION VARCHAR(255) NOT NULL
